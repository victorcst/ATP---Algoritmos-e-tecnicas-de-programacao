public class ex02 {
    public static void main(String[] args) {
        int num;

        for(num = 1; num <= 100; num++){
            System.out.println(num);
        }

        num=1;

        while(num<=100){
            System.out.println(num);
            num++;
        }

        num=0;

        do{
            System.out.println(num);
            num++;
        }while(num<=100);
    }
    
}
